fn main() {
    // addition
    let sum = 5 + 10;

    // subtraction
    let difference = 95 - 4;

    // multiplication
    let product = 4 * 30;

    // division
    let quotient = 56 / 32;
    let floored = 2 / 3; // Results in 0

    // remainder
    let remainder = 43 % 5;

    println!("{} {} {} {} {} {}", sum, difference, product, quotient, floored, remainder);
}